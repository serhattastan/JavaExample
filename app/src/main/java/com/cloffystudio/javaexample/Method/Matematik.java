package com.cloffystudio.javaexample.Method;

public class Matematik {
    public int topla(int sayi1, int sayi2) {
        return sayi1 + sayi2;
    }

    public double topla(double sayi1, double sayi2) {
        return sayi1 + sayi2;
    }

    public static int kareAl(int sayi) {
        return sayi * sayi;
    }
}
